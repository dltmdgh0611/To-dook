export default function AgentFeedback({ step, message }: AgentFeedbackProps) {
  const [displayedMessage, setDisplayedMessage] = useState('');
  const [isTyping, setIsTyping] = useState(false);
  
  const config = stepConfig[step];
  
  // 타이핑 애니메이션 효과
  useEffect(() => {
    if (!message) {
      setDisplayedMessage('');
      return;
    }
    
    setIsTyping(true);
    setDisplayedMessage('');
    
    let index = 0;
    const interval = setInterval(() => {
      if (index < message.length) {
        setDisplayedMessage(message.slice(0, index + 1));
        index++;
      } else {
        setIsTyping(false);
        clearInterval(interval);
      }
    }, 30);
    
    return () => clearInterval(interval);
  }, [message]);
  
  if (step === 'idle') return null;
  
  return (
    <div className="flex items-center gap-3 px-4 py-3 bg-gradient-to-r from-gray-50 to-gray-100 rounded-xl border border-gray-200 mb-4 animate-fadeIn">
      {/* 아이콘 */}
      <div className={`flex items-center justify-center w-8 h-8 rounded-full bg-white shadow-sm ${config.color}`}>
        {step !== 'complete' && step !== 'error' ? (
          <div className="animate-pulse">
            {config.icon}
          </div>
        ) : (
          config.icon
        )}
      </div>
      
      {/* 메시지 */}
      <div className="flex-1 min-w-0">
        <div className="flex items-center gap-2">
          <span className={`text-xs font-semibold uppercase tracking-wider ${config.color}`}>
            {config.label}
          </span>
          {isTyping && (
            <span className="flex gap-0.5">
              <span className="w-1 h-1 bg-gray-400 rounded-full animate-bounce" style={{ animationDelay: '0ms' }} />
              <span className="w-1 h-1 bg-gray-400 rounded-full animate-bounce" style={{ animationDelay: '150ms' }} />
              <span className="w-1 h-1 bg-gray-400 rounded-full animate-bounce" style={{ animationDelay: '300ms' }} />
            </span>
          )}
        </div>
        <p className="text-sm text-gray-700 mt-0.5 truncate">
          {displayedMessage}
          {isTyping && <span className="animate-pulse">|</span>}
        </p>
      </div>
      
      {/* 진행 표시기 */}
      {step !== 'complete' && step !== 'error' && (
        <div className="flex-shrink-0">
          <svg className="w-5 h-5 text-gray-400 animate-spin" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
            <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
            <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
          </svg>
        </div>
      )}
      
      <style jsx>{`
        @keyframes fadeIn {
          from {
            opacity: 0;
            transform: translateY(-10px);
          }
          to {
            opacity: 1;
            transform: translateY(0);
          }
        }
        
        .animate-fadeIn {
          animation: fadeIn 0.3s ease-out;
        }
      `}</style>
    </div>
  );
}

